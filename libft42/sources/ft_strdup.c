#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*str;
	int		l;
	int		i;

	i = 0;
	l = ft_strlen(s) + 1;
	str = malloc(sizeof(char) * l);
	if (!str)
		return (NULL);
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
